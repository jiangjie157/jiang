wanjia::wanjia()//pos 的XY  进行之前 做得的CAIQIE函数 
	:
	rand(rd()),//随机这个种子
	fenX(0, Graphics::max.x - lian.x),//给个范围
	fenY(0, Graphics::max.y - lian.y)
{	
	pos.x = fenX(rand);
	pos.y = fenY(rand);
	static int i = 0;//每次更换I 但是ID都被保留下来 所以当你再次使用ID这个时  id变为I  i会继续加1
	id = i;
	i++;
}

void wanjia::yidongA(const Keyboard& kbd)//常引用键盘
{
	if (kbd.KeyIsPressed(VK_UP))
		pos.y -=v.y;
	if (kbd.KeyIsPressed(VK_DOWN))
		pos.y += v.y;
	if (kbd.KeyIsPressed(VK_LEFT))
		pos.x -= v.x;
	if (kbd.KeyIsPressed(VK_RIGHT))
		pos.x += v.x;
}
